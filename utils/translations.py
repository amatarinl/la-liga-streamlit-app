def get_text(key, language="es", **kwargs):
    """
    Return the translated text for the given key and language.

    If the key does not exist, the function returns the key itself.
    Optional keyword arguments are used to format dynamic text.
    """
    text = TRANSLATIONS.get(language, TRANSLATIONS["es"]).get(key, key)

    if kwargs:
        return text.format(**kwargs)

    return text